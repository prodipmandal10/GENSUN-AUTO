# ---------- Download, extract & copy swarm.pem + temp-data ----------
download_extract_swarm() {
    echo -e "${GREEN}========== STEP 9: DOWNLOAD & EXTRACT SWARM.PEM ==========${NC}"
    DOWNLOAD_DIR="$HOME/pipe_downloads"
    ZIP_FILE="$DOWNLOAD_DIR/temp.zip"
    EXTRACT_DIR="$DOWNLOAD_DIR/extracted"
    mkdir -p "$DOWNLOAD_DIR" "$EXTRACT_DIR"

    # create temporary venv
    VENV_DIR="$DOWNLOAD_DIR/.venv_gdown"
    if [ ! -d "$VENV_DIR" ]; then
        python3 -m venv "$VENV_DIR"
    fi
    source "$VENV_DIR/bin/activate"

    pip install --upgrade pip
    pip install gdown

    if [ ! -f "$ZIP_FILE" ]; then
        read -p "ğŸ”— Enter Google Drive zip link: " ZIP_LINK
        ZIP_ID=$(echo "$ZIP_LINK" | grep -oP '(?<=/d/)[^/]+')
        echo -e "âš™ï¸ Downloading zip file..."
        gdown "https://drive.google.com/uc?id=$ZIP_ID" -O "$ZIP_FILE"
    else
        echo -e "âš ï¸ Zip already downloaded. Using cached copy."
    fi

    unzip -o "$ZIP_FILE" -d "$EXTRACT_DIR"

    echo -e "ğŸ“‚ Extracted folders:"
    folders=()
    i=1
    for f in "$EXTRACT_DIR"/*/; do
        [ -d "$f" ] || continue
        folders+=("$f")
        echo -e "${YELLOW}${BOLD}ğŸ“ $i) $(basename "$f")${NC}"
        ((i++))
    done

    read -p "ğŸ‘‰ Enter folder number to copy swarm.pem from: " sel
    SEL_FOLDER="${folders[$((sel-1))]}"

    # swarm.pem copy
    if [ -f "$SEL_FOLDER/swarm.pem" ]; then
        mkdir -p "$HOME/rl-swarm"
        cp -f "$SEL_FOLDER/swarm.pem" "$HOME/rl-swarm/"
        echo -e "${GREEN}âœ… swarm.pem copied to $HOME/rl-swarm/${NC}"
    else
        echo -e "${RED}âŒ swarm.pem not found in selected folder!${NC}"
    fi

    # temp-data copy (replace old)
    if [ -d "$SEL_FOLDER/temp-data" ]; then
        DEST="$HOME/rl-swarm/modal-login/temp-data"
        rm -rf "$DEST"
        mkdir -p "$(dirname "$DEST")"
        cp -r "$SEL_FOLDER/temp-data" "$HOME/rl-swarm/modal-login/"
        echo -e "${GREEN}âœ… temp-data copied & replaced at $HOME/rl-swarm/modal-login/temp-data/${NC}"
    else
        echo -e "${RED}âŒ No temp-data folder found in selected folder!${NC}"
    fi

    deactivate
}

